// ---- helpers ----

func repoRoot() string {
	// internal/genreadme/main.go тЖТ repo root is two dirs up from the source file's
	// working dir; `go run ./internal/genreadme` runs with CWD at the module root.
	wd, err := os.Getwd()
	if err != nil {
		fail("getwd: %v", err)
	}
	return wd
}

func mustRead(p string) []byte {
	b, err := os.ReadFile(p)
	if err != nil {
		fail("read %s: %v", p, err)
	}
	return b
}
func mustReadStr(p string) string { return string(mustRead(p)) }

func mustWrite(p, s string) { mustWriteBytes(p, []byte(s)) }
func mustWriteBytes(p string, b []byte) {
	if err := os.MkdirAll(filepath.Dir(p), 0o755); err != nil {
		fail("mkdir %s: %v", filepath.Dir(p), err)
	}
	if err := os.WriteFile(p, b, 0o644); err != nil {
		fail("write %s: %v", p, err)
	}
}

func fail(format string, a ...any) {
	fmt.Fprintf(os.Stderr, "genreadme: "+format+"\n", a...)
	os.Exit(1)
}

// relLinkRe matches a relative `](page.md)` link. Absolute `](httpтАж)` links never
// match (the `:` isn't in the char class); root-absolute `](/x.md)` links are
// skipped explicitly below.
var relLinkRe = regexp.MustCompile(`\]\(([\w./-]+?\.md)(#[\w-]+)?\)`)

func absolutize(text string) string {
	return relLinkRe.ReplaceAllStringFunc(text, func(m string) string {
		sub := relLinkRe.FindStringSubmatch(m)
		if strings.HasPrefix(sub[1], "/") {
			return m
		}
		return fmt.Sprintf("](%s/docs/pages/%s%s)", blob, sub[1], sub[2])
	})
}

func firstCode(md, lang string) string {
	re := regexp.MustCompile("(?s)```" + lang + "\\n(.*?)```")
	m := re.FindStringSubmatch(md)
	if m == nil {
		return ""
	}
	return strings.TrimRight(m[1], " \n\t")
}

func leadParagraphMd(md string) string {
	var out []string
	started := false
	for _, ln := range strings.Split(md, "\n") {
		if !started {
			started = strings.HasPrefix(ln, "# ")
			continue
		}
		s := strings.TrimSpace(ln)
		if s == "" {
			if len(out) > 0 {
				break
			}
			continue
		}
		if strings.IndexAny(string(s[0]), "#`>|-<") == 0 {
			if len(out) > 0 {
				break
			}
			continue
		}
		out = append(out, s)
	}
	return strings.TrimSpace(regexp.MustCompile(`\s+`).ReplaceAllString(strings.Join(out, " "), " "))
}

func leadParagraph(md string) string {
	s := leadParagraphMd(md)
	s = regexp.MustCompile("`([^`]+)`").ReplaceAllString(s, "$1")
	s = regexp.MustCompile(`\*\*(.+?)\*\*`).ReplaceAllString(s, "$1")
	s = regexp.MustCompile(`\[(.+?)\]\([^)]*\)`).ReplaceAllString(s, "$1")
	return strings.TrimSpace(s)
}
